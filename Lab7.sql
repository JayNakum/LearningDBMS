CREATE TABLE DEPT(
    DEPTNO INT(4) PRIMARY KEY,
    DNAME VARCHAR(20),
    LOC VARCHAR(20)
);

DESC DEPT;


CREATE TABLE EMP (
    EMP_NO INT(4) PRIMARY KEY,
    ENAME VARCHAR(20),
    JOB VARCHAR(20),
    MGR INT(4),
    HIREDATE DATE,
    SAL INT(8),
    COMM INT(4),
    DEPTNO INT(2), 
    FOREIGN KEY(DEPTNO) REFERENCES DEPT(DEPTNO)
);

DESC EMP;


